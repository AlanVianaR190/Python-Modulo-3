def aumentar(valor, porcent=0):
    resposta= valor + (valor * porcent/100)
    return resposta

#
def diminuir(valor, porcent=0):
    resposta= valor - (valor * porcent/100)
    return resposta

#
def dobro(valor):
    resposta= valor*2
    return resposta

#
def metade(valor):
    resposta= valor/2
    return resposta
